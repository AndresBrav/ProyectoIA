def pintarHorizontal(img,iniX,largo,iniY,ancho): 
    try:
        for i in range(iniX,iniX + largo):
            for j in range (iniY,iniY+ancho):
                img[j,i]=(0,0,255) 
    except Exception:
        print("error")
    
         
